_TAG_RE = re.compile(r"(?:^|\s)#([a-zA-Z\u4e00-\u9fff][\w/\u4e00-\u9fff]*)")
_FTS_SPECIAL = re.compile(r'["\*\(\)\-\^]')


def emit(data: dict[str, Any], json_mode: bool, human_lines: list[str] | None = None) -> None:
    """Print a CLI result in human or JSON form."""
    if json_mode:
        print(json.dumps(data, ensure_ascii=False, indent=2))
        return
    for line in human_lines or []:
        print(line)


def _reset_config_for_data_dir(data_dir: Path) -> None:
    """Point config loading at a data dir for the current process."""
    os.environ["BRAINDUMP_DATA_DIR"] = str(data_dir)
    import braindump.config as config_mod

    config_mod._config = None


